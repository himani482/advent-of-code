

def invalid_ID(data):
    ans = 0

    for item in data:
        start_s, end_s = item.split("-")
        start = int(start_s)
        end = int(end_s)

        for n in range(start, end + 1):
            s = str(n)
            l = len(s)

            for i in range(1, l// 2 +1):
                if l % i == 0:
                    if s == s[:i] * (l // i):
                        ans += int(s)
                        break

    return ans
